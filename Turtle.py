"""Create a Turtle Program that will draw a 3-dimensional cube"""
import turtle
cube = turtle.Turtle()
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.goto(25,25)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.left(90)
cube.forward(50)
cube.goto(50,0)
cube.left(90)
cube.forward(50)
cube.goto(75,75)
cube.left(90)
cube.forward(50)
cube.goto(0,50)
turtle.done()



